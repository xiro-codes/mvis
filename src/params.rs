#[derive(Clone, Copy, PartialEq, Eq, Debug, Serialize, Deserialize)]
pub enum AnimateSource {
    Off,
    Sine,
    Square,
    Triangle,
    Sawtooth,
    SubBass,
    Bass,
    LowMid,
    Mid,
    HighMid,
    High,
    Air,
}
//TODO impl to<u32> or what ever the gpu needs to avoid matching in ./gpu_pipeline
#[derive(Clone, Copy, PartialEq, Eq, Debug, Serialize, Deserialize)]
pub enum GravityWellPattern {
    None,
    Ring,
    Grid,
    Line,
    Spiral,
    Star,
    Cross,
    Random,
}

impl GravityWellPattern {
    pub fn name(&self) -> &'static str {
        match self {
            Self::None => "None",
            Self::Ring => "Ring",
            Self::Grid => "Grid",
            Self::Line => "Line",
            Self::Spiral => "Spiral",
            Self::Star => "Star",
            Self::Cross => "Cross",
            Self::Random => "Random",
        }
    }
}

#[derive(Clone, Copy, PartialEq, Eq, Debug, Serialize, Deserialize, Default)]
pub enum MusicInfoAnchor {
    TopLeft,
    TopRight,
    #[default]
    BottomLeft,
    BottomRight,
}

